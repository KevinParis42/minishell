#include "libft.h"

char	*ft_cutend(char *str, char c)
{
	int i;

	i = 0;
	while (str[i])
		i++;
	while (str[i] != c)
	{
		i--;
		if (i == 0)
			return (NULL);
	}
	str[i] = 0;
	return (str);
}
